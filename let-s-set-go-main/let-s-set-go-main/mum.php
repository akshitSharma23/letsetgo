      <center><h3 style="font-size:30;color:white;">Marine Drive</h3></center><br/>
	  <p>
      Marine Drive was built as part of the British government's Back Bay Reclamation Scheme during the second phase of Mumbai's urban development in the early 20th century. This scheme involved dredging the sea and dumping stone into it, to create land and expand the city westward. An inscription on a lamp post near Girgaum Chowpatty indicates that the construction of Marine Drive began there, at what was called Kennedy Sea Face, in 1915. It was named after Sir Michael Kavanagh Kennedy, an engineer who was Secretary of the Bombay Public Works Department and a General in the British Army. Unfortunately, he died in 1898, well before the reclamation works began.
    </p>
