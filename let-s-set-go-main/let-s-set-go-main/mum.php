      <center><h3 style="font-size:30;color:white;">The Bandra–Worli Sea Link</h3></center><br/>
	  <p> 
      The Bandra–Worli Sea Link is a 5.6 km long, 8-lane wide bridge that links Bandra in the Western Suburbs of Mumbai with Worli in South Mumbai. It is the 4th longest bridge in India after Bhupen Hazarika Setu, Dibang River Bridge and Mahatma Gandhi Setu. The Famous Bandra-Worli sea link is also known by the name Rajiv Gandhi Sea Link. It is a cable bridge that connects Bandra in the west of Mumbai to Worli which is in the southern part of the city. This bridge has helped in saving a lot of time for the passengers who travel from Bandra to Worli and vice versa.
    
